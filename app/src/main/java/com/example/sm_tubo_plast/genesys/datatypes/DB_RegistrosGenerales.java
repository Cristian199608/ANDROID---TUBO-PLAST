package com.example.sm_tubo_plast.genesys.datatypes;

public class DB_RegistrosGenerales {
    public int secuencia ;
    public String descripcion;
    public String estado ;
    public String adjetivo ;
    public int sec_ref ;
    
	public int getSecuencia() {
		return secuencia;
	}
	public void setSecuencia(int secuencia) {
		this.secuencia = secuencia;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getAdjetivo() {
		return adjetivo;
	}
	public void setAdjetivo(String adjetivo) {
		this.adjetivo = adjetivo;
	}
	public int getSec_ref() {
		return sec_ref;
	}
	public void setSec_ref(int sec_ref) {
		this.sec_ref = sec_ref;
	}

}
