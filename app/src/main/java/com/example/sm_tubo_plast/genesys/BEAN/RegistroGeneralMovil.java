package com.example.sm_tubo_plast.genesys.BEAN;

public class RegistroGeneralMovil {
	private String codDescripcion;
	private String descripcion;
	private String codValor;
	private String valor;
	
	public String getCodDescripcion() {
		return codDescripcion;
	}
	public void setCodDescripcion(String codDescripcion) {
		this.codDescripcion = codDescripcion;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	public String getCodValor() {
		return codValor;
	}
	public void setCodValor(String codValor) {
		this.codValor = codValor;
	}
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	
	

}
